// Функция циклического воспроизведения звука
void play_scream() {
    audoutInitialize();
    audoutStartAudioOut();
    uint32_t offset = 0;
    const uint32_t chunk = 4096 * 4;
    while (offset + chunk <= scream_bin_size) {
        uint32_t released = 0;
        AudioOutBuffer buf;
        memset(&buf, 0, sizeof(AudioOutBuffer));
        buf.buffer = (u32*)&scream_bin[offset];
        buf.buffer_size = chunk;
        buf.data_size = chunk;
        audoutAppendAudioOutBuffer(&buf);
        AudioOutBuffer* rel_buf = NULL;
        audoutWaitPlayFinish(&rel_buf, &released, 100000000);
        offset += chunk;
    }
    audoutStopAudioOut();
    audoutExit();
}

int main(int argc, char** argv) {
    Framebuffer fb;
    framebufferCreate(&fb, nwindowGetDefault(), 1280, 720, PIXEL_FORMAT_RGBA_8888, 2);
    framebufferMakeLinear(&fb);

    int current_level = 0;
    int player_x = 1, player_y = 1; 
    bool scream_triggered = false;

    while (appletMainLoop()) {
        PadState pad;
        padConfigureInput(1, HidNpadStyleSet_NpadStandard);
        padInitializeDefault(&pad);
        padUpdate(&pad);

        u64 kDown = padGetButtonsDown(&pad);
        if (kDown & HidNpadButton_Plus) break; // Выход на Плюс

        u32 stride;
        u32* framebuf = (u32*)framebufferBegin(&fb, &stride);

        if (!scream_triggered) {
            int next_x = player_x;
            int next_y = player_y;
            
            // Считывание нажатий кнопок d-pad геймпада
            if (kDown & HidNpadButton_Left)  next_x--;
            if (kDown & HidNpadButton_Right) next_x++;
            if (kDown & HidNpadButton_Up)    next_y--;
            if (kDown & HidNpadButton_Down)  next_y++;

            // Коллизия со стенами
            if (levels[current_level][next_y][next_x] != 1) {
                player_x = next_x;
                player_y = next_y;
            }

            // Наступление на финиш (3) -> переход на новый лвл
            if (levels[current_level][player_y][player_x] == 3) {
                current_level++;
                player_x = 1; player_y = 1;
            }

            // Наступление на ловушку скримера (4)
            if (levels[current_level][player_y][player_x] == 4) {
                scream_triggered = true;
            }

            // Отрисовка блоков лабиринта
            for (int y = 0; y < 720; y++) {
                for (int x = 0; x < 1280; x++) {
                    int cell_x = x / (1280 / MAZE_W);
                    int cell_y = y / (720 / MAZE_H);
                    
                    u32 color = RGBA8(15, 15, 15, 255); // Фон коридоров

                    if (cell_x < MAZE_W && cell_y < MAZE_H) {
                        int tile = levels[current_level][cell_y][cell_x];
                        if (tile == 1) color = RGBA8(60, 60, 65, 255);    // Стены (серые)
                        if (tile == 3) color = RGBA8(46, 204, 113, 255);  // Финиш (зеленый)
                    }

                    // Отрисовка игрока синим квадратиком
                    int block_w = 1280 / MAZE_W;
                    int block_h = 720 / MAZE_H;
                    int px = player_x * block_w + (block_w / 4);
                    int py = player_y * block_h + (block_h / 4);
                    if (x >= px && x < px + 30 && y >= py && y < py + 30) {
                        color = RGBA8(52, 152, 219, 255);
                    }

                    framebuf[y * stride / 4 + x] = color;
                }
            }

        } else {
            // ЭКРАН СКРИМЕРА: Швыряем зашитую текстуру монстра на экран приставки
            for (int i = 0; i < 1280 * 720; i++) {
                uint32_t pos = i * 3;
                framebuf[i] = RGBA8(scamer_bin[pos], scamer_bin[pos+1], scamer_bin[pos+2], 255);
            }
            framebufferEnd(&fb);

            // Запускаем звук крика (замораживает картинку монстра на экране пока орет)
            play_scream();
            
            scream_triggered = false; 
            current_level = 0; // Сбрасываем игру на 1 уровень после испуга
            player_x = 1; player_y = 1;
            continue;
        }

        framebufferEnd(&fb);
        consoleUpdate(NULL);
    }

    framebufferClose(&fb);
    return 0;
}
